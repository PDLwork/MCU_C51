#include <REGX52.H>
#include "UserDef.H"
#include "Delay.H"

#define LED P0
						 
/*******************************************************************************
* 函 数 名         :  main函数
* 函数功能		   :  主程循环执行
* 输    入         :  无
* 输    出         :  无
*******************************************************************************/
void main(void)
{
	INT8U i = 0;
	while(1)
	{
		LED = ~(0x80 >> i);
		i++;
		i %= 8; 
		DelayXms(100);
	}
}
