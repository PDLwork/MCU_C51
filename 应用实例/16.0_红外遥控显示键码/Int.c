#include <REGX52.H>
#include "UserDef.H"
#include "Int.H"

/*******************************************************************************
* 函 数 名		: Int0_Initialization（外部中断0初始化函数）
* 函数功能		: 外部中断初始化
* 输    入 		: 无
* 输    出		: 无
*******************************************************************************/
extern void Int0_Initialization(void)
{
	EA = 1;		//总中断打开
	EX0 = 1;   	//外部中断0打开
	IT0 = 1;	//0-低电平触发  1-下降沿触发
	IE0 = 0;	//标志位清零
}